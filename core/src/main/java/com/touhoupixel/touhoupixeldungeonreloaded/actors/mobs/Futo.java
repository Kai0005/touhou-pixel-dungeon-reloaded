import com.watabou.utils.Random;

public class Futo extends Mob {

    {
        spriteClass = FutoSprite.class;

        HP = HT = 76;
        defenseSkill = 22;
        EXP = 14;
        maxLvl = 30;

        properties.add(Property.WARP);

        loot = new SpellcardFragment();
        lootChance = 0.01f;
    }

    @Override
    public int damageRoll() {
        return Random.NormalIntRange(13, 19);
    }

    @Override
    public int attackSkill(Char target) {
        return 27;
    }

    @Override
    public int drRoll() {
        return Random.NormalIntRange(0, 2);
    }

    @Override
    public int attackProc(Char hero, int damage) {
        damage = super.attackProc(enemy, damage);
        if (enemy == Dungeon.hero && enemy.alignment != this.alignment) {
            Buff.affect(enemy, Burning.class).reignite(enemy, 10f);
            if (Statistics.difficulty > 2) {
                Buff.prolong(enemy, PotionFreeze.class, PotionFreeze.DURATION);
            }
            if (Statistics.difficulty > 4) {
                Buff.prolong(enemy, Silence.class, Silence.DURATION);
            }
        }
        return damage;
    }
}