public class Yamame extends Mob {

    {
        spriteClass = YamameSprite.class;

        HP = HT = 213;
        defenseSkill = 37;
        EXP = 19;
        maxLvl = 45;

        properties.add(Property.YOKAI);

        loot = new PotionOfToxicGas();
        lootChance = 0.1f;
    }

    @Override
    public int damageRoll() {
        return Random.NormalIntRange(24, 30);
    }

    @Override
    public int attackSkill(Char target) {
        return 42;
    }

    @Override
    public int drRoll() {
        return Random.NormalIntRange(0, 2);
    }

    @Override
    public int attackProc(Char hero, int damage) {
        damage = super.attackProc(enemy, damage);
        if (enemy == Dungeon.hero && enemy.alignment != this.alignment && Random.Int(4) == 0) {
            Buff.affect(hero, Poison.class).set(Math.round(Statistics.upgradesUsed/5f));
            if (Statistics.difficulty > 2) {
                Buff.prolong(enemy, AntiHeal.class, AntiHeal.DURATION);
            }
            if (Statistics.difficulty > 4) {
                Dungeon.hero.STR--;
            }
        }
        return damage;
    }
}